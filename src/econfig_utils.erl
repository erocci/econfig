mktemp_(TmpDir, Template) ->
    TmpFile = filename:join([TmpDir, Template ++ io_lib:format(".~p", [erlang:phash2(make_ref())])]),
    case filelib:is_regular(TmpFile) of
	false -> 
	    ok = file:write_file(TmpFile, []),
	    TmpFile;
	true ->
	    mktemp_(TmpDir, Template)
    end.

write(Filename, Data) ->
    Data2 = [ "%%%\n",
	      "%%% Generated by econfig on ", pp_timestamp(), "\n"
	      "%%%\n",
	      Data ],
    file:write_file(Filename, Data2).

loop_cmd(Port, Data, Timeout) ->
    receive
	{Port, {data, NewData}} ->
	    loop_cmd(Port, Data ++ NewData, Timeout);
	{Port, {exit_status, 0}} ->
	    {ok, Data};
	{Port, {exit_status, N}} ->
	    {error, {status, N}}
    after Timeout ->
	    {error, timeout}
    end.

canonical2([], Acc) ->
    filename:join(lists:reverse(Acc));
canonical2([".." | Tail], ["/"]) ->
    canonical2(Tail, ["/"]);
canonical2([".." | Tail], [_ | Acc]) ->
    canonical2(Tail, Acc);
canonical2(["." | Tail], Acc) ->
    canonical2(Tail, Acc);
canonical2([Name | Tail], Acc) ->
    canonical2(Tail, [Name | Acc]).
